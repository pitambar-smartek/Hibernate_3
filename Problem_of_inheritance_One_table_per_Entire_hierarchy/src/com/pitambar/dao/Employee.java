package com.pitambar.dao;
public class Employee
{
	private int empno;	
	private String name;
	
	
	//Setters and getters

	public void setEmpno(int empno)
	{
		this.empno=empno;
	}
	public int getEmpno()
	{
		return empno;
	}
	
	public void setName(String name)
	{
		this.name=name;
	}
	public String getName()
	{
		return name;
	}
	
}